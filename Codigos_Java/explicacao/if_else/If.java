public class If {

	public static void main(String[] args) {
		boolean estaChovendo = false;

		if (estaChovendo) {
			System.out.println("Hoje está chovendo. Vou ficar em casa.");
		} else {
			System.out.println("Hoje não está chovendo. Vou sair de casa.");
		}

	}

}
